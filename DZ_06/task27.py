# Задача 3. Измените код одной-двух решенных ранее задач
# (с прошлых семинаров или домашних работ), применив лямбды,
# filter, map, zip, enumerate, списочные выражения.
# ===========================================================

number = input("\nВведите вещественное число: ")
print("\nПрежнее решение.")
sum_digit = 0
for char in number:
    if char.isdigit():
        sum_digit += int(char)

print("  Сумма цифр введённого числа =", sum_digit)

print("\nРешение с lambda, map, filter: ")
res = list(map(lambda s: int(s), filter(lambda s: s.isdigit(), number)))
print("  Сумма цифр введённого числа =", sum(res))
print("\n===============================================================")

